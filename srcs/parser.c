#include "so_long.h"

size_t	count_wall(char *str)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i] == '1')
			j++;
        i++;
	}
	return (j);
}
